function createSlug($title) {

    $title = strip_tags($title); //Bỏ tag html

    $title = removeVietnameseAccent($title);

    // Bước 1: Chuyển tiêu đề sang chữ thường
    $title = mb_strtolower($title, 'UTF-8');

    // Bước 3: Loại bỏ các ký tự đặc biệt
    $title = preg_replace('/[^a-z0-9\s-]/u', '', $title);

    // Bước 4: Thay thế khoảng trắng bằng dấu gạch ngang (-)
    $title = preg_replace('/[\s-]+/', '-', $title);

    // Bước 5: Xóa dấu gạch ngang ở đầu và cuối chuỗi (nếu có)
    $title = trim($title, '-');

    return $title;
}

